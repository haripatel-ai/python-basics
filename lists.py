# Day 5 - Lists in Python

# 1. Create a list
numbers = [10, 20, 30, 40, 50]

print("List:", numbers)

# 2. Access elements
print("First element:", numbers[0])
print("Last element:", numbers[-1])

# 3. Loop through list
print("All elements:")
for num in numbers:
    print(num)

# 4. Length of list
print("Length of list:", len(numbers))

# 5. Change element
numbers[2] = 35
print("Updated list:", numbers)